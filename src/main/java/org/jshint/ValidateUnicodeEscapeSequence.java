    private ValidateUnicodeEscapeSequence() {
    }

    public static boolean validate(String sequence) {
        int equalSignIndex = sequence.indexOf("=");

        if (equalSignIndex == -1) {
            return names.get("binary").containsKey(sequence) || values.get("general").containsKey(sequence);
        }

        String name = sequence.substring(0, equalSignIndex);
        String value = sequence.substring(equalSignIndex + 1);
        if (name.equals("General_Category") || name.equals("gc")) {
            return values.get("general").containsKey(value);
        }
        if (name.equals("Script") || name.equals("sc") || name.equals("Script_Extensions") || name.equals("scx")) {
            return values.get("script").containsKey(value);
        }

        return false;
    }
